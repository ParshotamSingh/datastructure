public class GeometricalCalByPri {
    public int areaofcircle(int radius){
        return (22/7)*radius*radius;
    }

    public int diameterofcircle(int radius){
        return 2*radius;
    }

    public int areaofsquare(int side){
        return side*side;
    }

    public int areaofrectangle(int length, int breadth){
        return length*breadth;
    }
}
